//Write a Java program to declare variables of all primitive data types and print their values.
class Datatypes
{
	public static void main(String args[])
	{
		byte b = 105;
		short s = 250;
		int i = 1005;
		long l = 255567;
		float f  = 35.98f;
		double d = 2559.23;
		char c = 'A';
		boolean bool = true;

		System.out.println("Byte is "+ b);
		System.out.println("Short is "+ s);
		System.out.println("Int is "+ i);
		System.out.println("Long is "+ l);
		System.out.println("Float is "+ f);
		System.out.println("Double is "+ d);
		System.out.println("Character is "+ c);
		System.out.println("Boolean is "+ bool);

	}
}

/*Output:
Byte is 105
Short is 250
Int is 1005
Long is 255567
Float is 35.98
Double is 2559.23
Character is A
Boolean is true
*/